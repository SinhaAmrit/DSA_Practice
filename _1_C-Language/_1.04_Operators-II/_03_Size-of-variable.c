//? Write a program to print size of an int, a float, a char and a double type variable
#include <stdio.h>
int main()
{
    printf("Size of an int variable is %d\n", sizeof(int));
    printf("Size of a float variable is %d\n", sizeof(float));
    printf("Size of a char variable is %d\n", sizeof(char));
    printf("Size of a double variable is %d\n", sizeof(double));
    return 0;
}